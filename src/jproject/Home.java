    final void updateTicket() {
        try {
            stmt = conn.createStatement();
            rs = stmt.executeQuery("select * from ticket");
            while(rs.next()) {
                String st1 = rs.getString(7)+" "+rs.getString(9).split(" ")[0];
                Date d1;
                Date d2;
                d1 = new SimpleDateFormat("dd/MM/yyyy HH:mm").parse(st1);
                String st2 = new SimpleDateFormat("dd/MM/yyyy HH:mm").format(Calendar.getInstance().getTime());
                d2 = new SimpleDateFormat("dd/MM/yyyy HH:mm").parse(st2);
                if(d1.before(d2))
                    stmt.executeUpdate("update ticket set type=1 where id="+rs.getInt(1));
            }
        } catch (SQLException | ParseException e) {
            e.printStackTrace();
        }
    }
